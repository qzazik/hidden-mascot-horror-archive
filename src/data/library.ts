const mediaFor = (slug: string) => {
  const entry = (assetReport as {
    games?: Record<string, { mainImage?: string | null; gallery?: string[] }>;
  }).games?.[slug];

  return {
    mainImage: entry?.mainImage ?? null,
    gallery: entry?.gallery ?? []
  };
};

